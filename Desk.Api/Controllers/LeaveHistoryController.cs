        #region fields
        private readonly ILeaveHistoryService _leaveHistoryService;
        #endregion
        #region ctor
        public LeaveHistoryController(ILeaveHistoryService leaveHistoryService)
        {
            _leaveHistoryService = leaveHistoryService;
        }
        #endregion

        #region methods

        [HttpGet]
        [Route("Get")]
        public async Task<IActionResult> Get()
        {
            try
            {
                var results = await _leaveHistoryService.GetAllAsync();
                return Ok(new ResponseResult { Result = results, IsSuccess = true, Message = "" });
            }
            catch (Exception ex)
            {
                return Ok(new ResponseResult { Result = null, IsSuccess = false, Message = ex.Message });
            }

        }


