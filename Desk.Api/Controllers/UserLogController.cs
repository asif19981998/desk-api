            }
            catch (Exception ex)
            {
                return Ok(new ResponseResult { Result = null, IsSuccess = false, Message = ex.Message });
            }
        }


        [HttpPost]
        [Route("PermanentDelete")]
        public async Task<IActionResult> HardDelete(UserLog userLog)
        {
            try
            {
                var result = await _userLogService.PermanentDeleteAsync(userLog);
